/** Operator */

void main(){
  int numA = 8;
  int numB= -3;
  print('-------------- Relational Operators --------------------');
  print('numA($numA) > numB($numB):  ' +  (numA > numB).toString() );
  print('numA($numA) < numB($numB):  ' +  (numA < numB).toString() );
  print('numA($numA) >= numB($numB): ' + (numA >= numB).toString() );
  print('numA($numA) <= numB($numB): ' + (numA <= numB).toString() );
  print('numA($numA) == numB($numB): ' + (numA == numB).toString() );
  print('numA($numA) != numB($numB): ' + (numA != numB).toString() );
}
