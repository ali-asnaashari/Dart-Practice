/** if - else if - else in Dart Programming Language  **/

void main(){

  int numA = 10;
  if (numA < 9) {
    print("Condition 1 is true");
    numA++;
  }
  else if (numA < 10) {
    print("Condition 2 is true");
  }
  else if (numA >= 10) print("Condition 3 is true");

  else {
      print("All the conditions are false");
  }
}

